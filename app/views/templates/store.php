<h1>Tienda</h1>
<h3>¡La estamos desarrollando y estará en breve!</h3>
<p>La Tienda va a acercar a desarrolladores y creativos la manera más fácil para vender y distribuir su producto sin comisiones.</p>
<p>Nuestro objetivo es que ganes puntos para poder reinvertirlos en recursos para tu proyecto.</p>
<h4>¿Qué clases de proyectos buscan proteger y maximizar?</h4>
<img src="https://briff.me/wp-content/uploads/2015/12/Star-Wars-VII-The-Force-Awakens-22-Rey-help-Finn-on-Jakku.jpeg" width="100%" />
<p>Cualquier proyecto que incluya una computadora puede recibir ayuda de la comunidad fácilmente. Ya sea que estás redactando y necesitás 
un lugar dónde todos te vean cómo si buscás gente para sumar al desarrollo de tu videojuego o idea innovadora. Todos estamos acá.</p>
<h4>¿Recursos para mi proyecto?</h4>
<p>Queremos estar presentes en todos los sectores de tu proyecto, <strong>para ayudarte a hacer más con menos plata</strong>. </p>
<h4>Socios y métodos de pago</h4>
<p>Por el momento no tenemos socios. ¡<a href="developers">Informate sobre la API de holanerd</a> e integrate a nuestro método
de pago!</p>
<p>Queremos transformar los puntos que ganás a partir de posts y ventas en una moneda más. Una moneda que la vas a ganar según el 
tiempo que dediques al sitio y cómo la gente perciba tus aportes. Si les gusta, te dejan puntos. El concepto es sencillo.</p>
